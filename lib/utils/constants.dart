Role roleParser({required int role}) {
  switch (role) {
    case 20:
      return Role.admin;
    case 15:
      return Role.member;
    case 10:
      return Role.viewer;
    case 5:
      return Role.guest;
    default:
      return Role.none;
  }
}

int priorityParser(String priority) {
