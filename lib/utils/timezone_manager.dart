    // if (timeToReturn.minute >= 60) {
    //   timeToReturn.hour += 1;
    //   timeToReturn.minute -= 60;
    // }

    SimpleTime timeToReturn = SimpleTime(time.hour, time.minute);

    return timeToReturn;
  }

  int hour;
  int minute;
  @override
  String toString() {
    String hourInString = hour.toString();
    String minuteInString = minute.toString();
    if (hour < 10 && hour > 0) {
      hourInString = '0$hour';
    }
    if (minute < 10) {
      minuteInString = '0$minute';
    }
    return '$hourInString:$minuteInString ${isPm ? 'PM' : 'AM'} ';
  }
